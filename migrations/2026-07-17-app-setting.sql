begin;

create table if not exists ods_setting (
  /** ລະຫັດການຕັ້ງຄ່າ — ນິຍາມຢູ່ src/lib/settings.ts (ຢ່າພິມສົດ) */
  key        varchar(50)  primary key,
  /** ຄ່າເປັນ text — boolean ໃຊ້ 'on' / 'off' */
  value      varchar(200) not null,
  updated_by varchar(100),
  updated_at timestamp    not null default localtimestamp(0)
);

comment on table ods_setting is
  'ການຕັ້ງຄ່າລະບົບ (key/value) ທີ່ຜູ້ຈັດການແກ້ໄດ້ — ບໍ່ມີແຖວ = ໃຊ້ຄ່າຕັ້ງຕົ້ນຂອງໂຄ້ດ. ເບິ່ງ src/lib/settings.ts';

commit;
