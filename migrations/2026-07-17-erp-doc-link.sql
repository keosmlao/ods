-- ດັດຊະນີ "ວຽກ ↔ ໃບ ERP" ທີ່ລະບົບນີ້ອອກເອງ
--
-- ── ເປັນຫຍັງຕ້ອງມີ ──
-- ຕອນອອກໃບລົງ ERP ເຮົາຂຽນເລກວຽກໄວ້ໃນ `ic_trans.doc_ref` (+ `remark`) ຂອງ ERP.
-- ແຕ່ **ຄົນ ERP ແກ້ຊ່ອງເຫຼົ່ານັ້ນໄດ້** — ແລະ ເກີດຂຶ້ນຈິງແລ້ວພາຍໃນມື້ດຽວ:
-- ໃບ SPR26070008 (ຂອງວຽກ 7521) ຖືກເປີດແກ້ໃນ ERP ຫຼັງອະນຸມັດ ⇒ doc_ref ຖືກລ້າງ,
-- remark ຖືກຂຽນທັບ, ອາໄຫຼ່ຖືກປ່ຽນ ⇒ **ວຽກກັບໃບຂາດຈາກກັນຢ່າງງຽບໆ**.
-- ຜົນ: ໜ້າຈໍຂຶ້ນ "ບໍ່ພົບໃນ ERP" ແລ້ວ**ຍື່ນປຸ່ມ "ຍົກເລີກສັ່ງຊື້" ໃຫ້ກົດ** — ກົດແລ້ວ
-- ຈະລຶບການສັ່ງຊື້ຈິງທີ່ອະນຸມັດແລ້ວຖິ້ມ. ຄວາມຈິງທີ່ຝາກໄວ້ກັບຄົນອື່ນ ຮັກສາເອງບໍ່ໄດ້.
--
-- ── ນີ້ຂັດກັບ "ທຸກຢ່າງຢູ່ ERP" ບໍ ── ບໍ່.
-- ຄຳສັ່ງນັ້ນຄື **ເອກະສານ**ຢູ່ ERP ບ່ອນດຽວ — ຍັງເປັນແບບນັ້ນ. ບ່ອນນີ້ເກັບພຽງ
-- **ຕົວຊີ້** (ວຽກ 7521 → SPR26070008): ບໍ່ມີລາຍການ, ບໍ່ມີລາຄາ, ບໍ່ມີຈຳນວນ
-- ⇒ ບໍ່ມີທາງຂັດແຍ້ງກັບ ERP ເພາະບໍ່ໄດ້ເກັບຄວາມຈິງອັນດຽວກັນຊ້ຳ. ມັນຕອບຄຳຖາມດຽວ
-- ທີ່ ERP ຕອບແທນເຮົາບໍ່ໄດ້: "ໃບໃດທີ່ **ລະບົບນີ້** ອອກໃຫ້ວຽກນີ້".
--
-- ⚠️ ໃບເກົ່າບໍ່ມີໃນນີ້ — ຕົວອ່ານຕ້ອງໃຊ້ doc_ref/remark/RQ ເປັນທາງສຳຮອງຕໍ່ໄປ.

create table if not exists ods_erp_doc_link (
  doc_no     varchar   not null,
  trans_flag int       not null,
  job_code   varchar   not null,
  created_by varchar   not null default '',
  created_at timestamp not null default localtimestamp(0),
  primary key (doc_no, trans_flag)
);

comment on table ods_erp_doc_link is
  'ວຽກ ↔ ໃບ ERP ທີ່ລະບົບນີ້ອອກ (ດັດຊະນີເທົ່ານັ້ນ — ເອກະສານຈິງຢູ່ ERP ບ່ອນດຽວ)';

create index if not exists ods_erp_doc_link_job_idx on ods_erp_doc_link(job_code);
