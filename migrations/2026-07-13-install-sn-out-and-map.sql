-- ═══════════════════════════════════════════════════════════════════
--  ງານຕິດຕັ້ງ: ① S/N ໜ່ວຍນອກ (ແອ)  ② ພິກັດແຜນທີ່ຂອງສະຖານທີ່ຕິດຕັ້ງ
--  ຖານ: ODS ເທົ່ານັ້ນ · ເພີ່ມຖັນຢ່າງດຽວ (ບໍ່ແຕະຂໍ້ມູນເກົ່າ)
-- ═══════════════════════════════════════════════════════════════════

begin;

-- ── ① ແອ = 2 ໜ່ວຍ ⇒ 2 S/N ──────────────────────────────────────────
--
-- ods_tb_install ມີ **pro_sn ຖັນດຽວ** ແຕ່ແອປະກອບດ້ວຍ:
--   ໜ່ວຍໃນ [C] (ຄອຍລ໌ເຢັນ) + ໜ່ວຍນອກ [H] (ຄອມເພຣສເຊີ)
-- ແລະ ERP ລົງ ISN **ຄົນລະເລກ** ໃຫ້ສອງໜ່ວຍ (ບິນ CAK26008714: ໜ່ວຍໃນ 032A0028508 ·
-- ໜ່ວຍນອກ 032A0000558). ດຽວນີ້ເກັບໄດ້ໜ່ວຍດຽວ ⇒ ຮັບປະກັນ/ສ້ອມພາຍຫຼັງ ອ້າງອີງ
-- ໜ່ວຍນອກບໍ່ໄດ້ ແລະ ກວດບໍ່ໄດ້ວ່າຊ່າງຕິດຄອມເພຣສເຊີໜ່ວຍໃດໃສ່ບ້ານໃຜ.
--
-- pro_sn ຄືເກົ່າ = **ໜ່ວຍໃນ** (ຕົວທີ່ຖືເປັນເອກະລັກຂອງເຄື່ອງ) ⇒ ຂໍ້ມູນເກົ່າຄວາມໝາຍບໍ່ປ່ຽນ.
alter table ods_tb_install add column if not exists pro_sn_out varchar(100);

comment on column ods_tb_install.pro_sn_out is
  'S/N (ຫຼື ISN) ຂອງໜ່ວຍນອກ [H] — ສະເພາະແອ. pro_sn = ໜ່ວຍໃນ [C]';


-- ── ② ພິກັດຂອງສະຖານທີ່ຕິດຕັ້ງ ────────────────────────────────────
--
-- location_inst ເປັນຂໍ້ຄວາມລ້ວນ ("ບ້ານ ໂພນທັນ, ເມືອງ ໄຊເສດຖາ") ⇒ ຊ່າງຕ້ອງໂທຖາມທາງ
-- ທຸກເທື່ອ ແລະ ຄິວປະຈຳວັນຈັດເສັ້ນທາງບໍ່ໄດ້. ເກັບພິກັດໄວ້ ⇒ ແອັບຊ່າງກົດນຳທາງໄດ້ເລີຍ
-- (ແອັບມີ GPS ຢູ່ແລ້ວສຳລັບ check-in — ໃຊ້ຄູ່ກັນເພື່ອກວດວ່າໄປຮອດຈຸດທີ່ນັດຫຼືບໍ່).
--
-- ຫວ່າງໄດ້ (ບໍ່ບັງຄັບ) — ບິນທີ່ບໍ່ຮູ້ພິກັດຍັງເປີດງານໄດ້ຄືເກົ່າ.
alter table ods_tb_install add column if not exists location_lat double precision;
alter table ods_tb_install add column if not exists location_lng double precision;

comment on column ods_tb_install.location_lat is 'ພິກັດສະຖານທີ່ຕິດຕັ້ງ (ບໍ່ບັງຄັບ) — ໃຊ້ນຳທາງ ແລະ ທຽບກັບ check-in';

commit;
